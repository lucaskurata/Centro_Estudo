#Serve para transformar uma variável para Inteiro, Float, String, Array ou Símbolo
#TO_I: Converte a variável em Int
#TO_F: Converte a variável em Float
#TO_s: Converte a variável em String
#TO_A: Converte a variável em Array
#TO_SYM: Converte a variável em Símbolo

variavel_int = 100
variavel_float = 10.10
variavel_str = '2020'

puts "Tranformando Inteiro em String: " + variavel_int.to_s
puts "Transformando Float em String: " + variavel_float.to_s
puts "Transformando String em Int: " + variavel_str.to_i