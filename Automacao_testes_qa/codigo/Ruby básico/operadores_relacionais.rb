#Menor: <
#Maior igual: >=
#Menor igual: <=
#Igual: ==
#Diferente: !=

#Spaceship: <=> 
# -1 -> Quando o elemento da direita é maior do que a esquerda
# 0 -> Quando os elementos são iguais
# 1 -> Quando o elemento da esquerda é maior do que a direita

puts "2 é maior do que 1? #{2 > 1}" 
puts "2 é maior do que 3? #{2 > 3}" 
puts "3 é maior igual do que 2? #{3 >= 2}" 
puts "2 é menor igual do que 1? #{2 <= 1}" 
puts "10 é igual 10? #{10 == 10}"
puts "10 é igual 11? #{10 == 11}"
puts "2 é diferente de 10? #{2 != 10}"
puts "2 é diferente de 2? #{2 != 2}"

