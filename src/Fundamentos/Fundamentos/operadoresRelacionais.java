package Fundamentos;

public class operadoresRelacionais {
	public static void main(String[] args) {
		//==, !=, <=, <, >, >=
		int num1 = 5;
		int num2 = 10;
		System.out.println(num1 != num2);
		System.out.println(num1>= num2);
		System.out.println(num2 >= num1);
		System.out.println(num1 == num2);
		int soma = num1 + num2;
		int num3 = soma;
		System.out.println(num3);
		
		
	}
}
